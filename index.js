/* add function, add's two floats a and b and returns the answer
*/
function add(a,b) {
  return a + b
}

/* subtract, subtracts two floats a from b and returns the answer
*/
function subtract(a,b) {
  return a-b 
}

/* multiply, multiplys two floats and returns the answer
*/
function multiply(a,b) {
  return a*b
}